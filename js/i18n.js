// Current language
let currentLang = 'en';

function applyTranslations(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS.en;
  currentLang = lang;

  // Apply to all data-i18n elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      // Some elements allow HTML (about.p*)
      if (key.startsWith('about.p')) {
        el.innerHTML = t[key];
      } else {
        el.textContent = t[key];
      }
    }
  });

  // Set dir for Arabic
  const isRTL = lang === 'ar';
  document.documentElement.setAttribute('dir', isRTL ? 'rtl' : 'ltr');
  document.documentElement.setAttribute('lang', lang);

  // Update current lang button
  const labels = { en: 'EN', fr: 'FR', ar: 'AR', sw: 'SW', am: 'AM' };
  const el = document.getElementById('currentLang');
  if (el) el.textContent = labels[lang] || 'EN';

  // Update active lang option
  document.querySelectorAll('.lang-opt').forEach(btn => {
    btn.classList.remove('active');
    const onclick = btn.getAttribute('onclick') || '';
    if (onclick.includes(`'${lang}'`)) btn.classList.add('active');
  });

  // Save preference
  try { localStorage.setItem('sq_lang', lang) } catch(e) {}
}

function setLang(lang) {
  applyTranslations(lang);
  // Close dropdown
  const dd = document.getElementById('langDropdown');
  if (dd) dd.classList.remove('open');
}

// Auto-detect language on load
async function detectLanguage() {
  // Check saved preference first
  try {
    const saved = localStorage.getItem('sq_lang');
    if (saved && TRANSLATIONS[saved]) {
      applyTranslations(saved);
      return;
    }
  } catch(e) {}

  // Try IP-based detection (only allowed external service)
  try {
    const res = await fetch('https://ip-api.com/json/?fields=countryCode', { signal: AbortSignal.timeout(3000) });
    const data = await res.json();
    const cc = data.countryCode || '';

    // Map country codes to languages
    const ARABIC = ['EG','DZ','MA','TN','LY','SD','MR','SO','DJ','ER','KM','TD'];
    const FRENCH = ['FR','CI','SN','ML','BF','GN','NE','CM','CG','GA','CF','TD','MG','GW','KM','BI','DJ'];
    const SWAHILI = ['KE','TZ','UG','RW','BI','CD'];
    const AMHARIC = ['ET'];

    let lang = 'en';
    if (AMHARIC.includes(cc)) lang = 'am';
    else if (ARABIC.includes(cc)) lang = 'ar';
    else if (SWAHILI.includes(cc)) lang = 'sw';
    else if (FRENCH.includes(cc)) lang = 'fr';

    applyTranslations(lang);
  } catch(e) {
    // Fallback to English
    applyTranslations('en');
  }
}

// Init
detectLanguage();
